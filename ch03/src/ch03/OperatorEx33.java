package ch03;

public class OperatorEx33 {
	public static void main(String[] args) {
		int i = 0;
		i = i + 3;
		System.out.println(i);
		
		i = 0;
		i += 3;
		System.out.println(i);
		
		i = 0;
		i = i - 3;
		System.out.println(i);
		
		i = 0;
		i -= 3;
		System.out.println(i);
		
		i = 0;
		i *= 3; // i = i * 3; 과 동일
		System.out.println(i);
		
		i = 30;
		i /= 3; // i = i / 3; 과 동일
		System.out.println(i);
		
	}

}
